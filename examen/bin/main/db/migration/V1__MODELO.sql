CREATE TABLE TASE_ASEGURADOS (
NM_ID INT NOT NULL AUTO_INCREMENT,
DS_DOCUMENTO VARCHAR(12) NOT NULL,
DS_NOMBRE VARCHAR(45) NULL,
VM_SALARIO DECIMAL(14,2) NOT NULL DEFAULT 0.00,
VM_CREDITOS DECIMAL(14,2) NOT NULL DEFAULT 0.00,
VM_GASTOS_FIJOS DECIMAL(14,2) NOT NULL DEFAULT 0.00,
PRIMARY KEY(NM_ID)
);

CREATE TABLE TASE_POLIZAS (
NM_ID INT NOT NULL AUTO_INCREMENT,
VM_VALOR DECIMAL(14,2) NOT NULL DEFAULT 0.00 COMMENT 'VALOR BASE',
PRIMARY KEY(NM_ID)
);

CREATE TABLE TASE_VEHICULOS (
NM_ID INT NOT NULL AUTO_INCREMENT,
DS_PLACA VARCHAR(6) NOT NULL,
NM_MODELO INT NOT NULL,
CD_MARCA VARCHAR(12) NOT NULL,
NM_CANTIDAD_PASAJEROS INT NOT NULL,
PRIMARY KEY(NM_ID)
);

CREATE TABLE TASE_AFILIACIONES (
NM_ID INT NOT NULL AUTO_INCREMENT,
NM_ID_VEHICULO INT NOT NULL,
NM_ID_ASEGURADO INT NOT NULL,
NM_ID_POLIZA INT NOT NULL,
PRIMARY KEY(NM_ID),
CONSTRAINT FK_AFILIACION_VEHICULO
FOREIGN KEY (NM_ID_VEHICULO)
REFERENCES TASE_VEHICULOS (NM_ID),
CONSTRAINT FK_AFILIACION_ASEGURADO
FOREIGN KEY (NM_ID_ASEGURADO)
REFERENCES TASE_ASEGURADOS (NM_ID),
CONSTRAINT FK_AFILIACION_POLIZA
FOREIGN KEY (NM_ID_POLIZA)
REFERENCES TASE_POLIZAS (NM_ID)
);